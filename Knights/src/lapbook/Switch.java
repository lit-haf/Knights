package lapbook;

public class Switch {

	public static void main(String[] args) {
		int tech=2;
		switch(tech)
		{
		case 1:
		{
			System.out.println("Java");
			break;
		}
		case 2:
		{
			System.out.println("Es6");
			break;
		}
		default:
		{
			System.out.println("Not Listed");
		}
		}

	}

}
